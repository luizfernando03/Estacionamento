package com.ada.exercicios;

public class Veiculo {
    private String placa;
    private String Tipo;

    public Veiculo(String placa, String tipo){
        this.placa = placa;
        this.placa = tipo;
    }

    public String getPlaca() {
        return placa;
    }

    public String getTipo() {
        return Tipo;
    }
}
